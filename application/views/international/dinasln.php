 <strong> SURAT PERSETUJUAN PERJALANAN DINAS LUAR NEGERI (PDLN) DARI KEMENTERIAN SEKRETARIAT NEGARA – SP SETNEG </strong>
  <br>

Sesuai dengan PP 45 Tahun 2013 tentang Tata Cara Pelaksanaan APBN (Khususnya Pasal 90) dan Peraturan Menteri Keuangan nomor 164/PMK.05/2015 tentang Tata Cara Pelaksanaan Perjalanan Dinas Luar Negeri sebagaimana telah direvisi dengan Peraturan Menteri Keuangan nomor 227/PMK.05/2016, maka terkait perjalanan dinas ke luar negeri (PDLN) terdapat ketentuan-ketentuan sebagai berikut:
<br>
1.Perjalanan dinas luar negeri terlebih dahulu memerlukan izin Presiden atau pejabat yang ditunjuk.<br>
2.Izin Presiden atau pejabat yang ditunjuk diterbitkan melalui Kementerian Sekretariat Negara.<br>
3.Permohonan izin perjalanan dinas ke luar negeri diajukan paling lambat 1 (satu) bulan sebelum keberangkatan<br>
4.Apabila sampai dengan tanggal keberangkatan yang diusulkan belum mendapat persetujuan tertulis dari pejabat yang ditunjuk, maka (secara aturan) yang bersangkutan tidak diizinkan melaksanakan perjalanan dinas ke luar negeri.<br>
5.Perjalanan dinas luar negeri dengan biaya negara (APBN/APBD) dilaksanakan dengan mengutamakan perusahaan penerbangan nasional atau perusahaan pengangkutan nasional lainnya<br>
6.Perjalanan dinas ke negara yang belum memiliki hubungan diplomatik dengan Indonesia, diperlukan rekomendasi dari Departemen Luar Negeri<br>
7.Perjalanan dinas luar negeri dengan biaya donor luar/dalam negeri tidak menimbulkan ikatan apapun terhadap Pemerintah<br>
Saat ini, Kementerian Sekretariat Negara telah membuat aplikasi layanan online Sistem Informasi Perjalanan Dinas Luar Negeri  <a href="https://simpel.setneg.go.id">(Link)</a>.
